// Function to sort offers by price (lowest to highest)
export const sortOffersByPrice = (offers: any[]) => {
  return [...offers].sort((a, b) => {
    // Only sort offers with price
    if (a.price && b.price) {
      // Extract numeric values from price strings
      const priceA =
        parseFloat(a.price.replace(/[^\d,]/g, "").replace(",", ".")) || 0;
      const priceB =
        parseFloat(b.price.replace(/[^\d,]/g, "").replace(",", ".")) || 0;
      return priceA - priceB;
    }
    // If one has price and the other doesn't, prioritize the one with price
    if (a.price && !b.price) return -1;
    if (!a.price && b.price) return 1;
    // If neither has price, maintain original order
    return 0;
  });
};

// Get sorted offers based on sort method
export const getSortedOffers = (offers: any[], sortMethod: string) => {
  if (sortMethod === "price") {
    return sortOffersByPrice(offers);
  }
  // Default is "recommended" which is the original order
  return offers;
};
